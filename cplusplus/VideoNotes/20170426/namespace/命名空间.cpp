/************************************************************
   (1)C语言出现与全局变量重名则无法引用全局变量。
************************************************************/
#include <iostream>

int a = 1;

int main()
{
    int a = 3;

    std::cout << ::a << std::endl; /* :: 默认认为为全局域 */
    std::cout << a << std::endl;

    system("pause");
    return 0;
}